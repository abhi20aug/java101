package com.designPatterns.A_creational.builder.examples.yogaStudent.model;

public class Yoga {

 private boolean Yam;
 private boolean Niyam;
 private boolean Aasan;
 private boolean Pranayam;
 private boolean Pratyahar;
 private boolean Dhyan;
 private boolean Dharna;
 private boolean Samadhi;

 public boolean isYam() {
  return Yam;
 }

 public void setYam(boolean yam) {
  Yam = yam;
 }

 public boolean isNiyam() {
  return Niyam;
 }

 public void setNiyam(boolean niyam) {
  Niyam = niyam;
 }

 public boolean isAasan() {
  return Aasan;
 }

 public void setAasan(boolean aasan) {
  Aasan = aasan;
 }

 public boolean isPranayam() {
  return Pranayam;
 }

 public void setPranayam(boolean pranayam) {
  Pranayam = pranayam;
 }

 public boolean isPratyahar() {
  return Pratyahar;
 }

 public void setPratyahar(boolean pratyahar) {
  Pratyahar = pratyahar;
 }

 public boolean isDhyan() {
  return Dhyan;
 }

 public void setDhyan(boolean dhyan) {
  Dhyan = dhyan;
 }

 public boolean isDharna() {
  return Dharna;
 }

 public void setDharna(boolean dharna) {
  Dharna = dharna;
 }

 public boolean isSamadhi() {
  return Samadhi;
 }

 public void setSamadhi(boolean samadhi) {
  Samadhi = samadhi;
 }
}

