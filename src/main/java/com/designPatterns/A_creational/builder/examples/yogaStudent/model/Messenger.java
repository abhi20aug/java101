package com.designPatterns.A_creational.builder.examples.yogaStudent.model;

public class Messenger {
    private String messengerAppl;
    private String messengerAddr;

    public String getMessengerAppl() {
        return messengerAppl;
    }

    public void setMessengerAppl(String messengerAppl) {
        this.messengerAppl = messengerAppl;
    }

    public String getMessengerAddr() {
        return messengerAddr;
    }

    public void setMessengerAddr(String messengerAddr) {
        this.messengerAddr = messengerAddr;
    }
}
